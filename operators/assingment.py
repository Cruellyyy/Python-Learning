x = 10
print("Initial x:", x)

x += 5
print("After += 5:", x)  # 15

x -= 3
print("After -= 3:", x)  # 12

x *= 2
print("After *= 2:", x)  # 24

x /= 4
print("After /= 4:", x)  # 6.0

x //= 2
print("After //= 2:", x)  # 3.0

x %= 2
print("After %= 2:", x)  # 1.0

x = 3
x **= 3
print("After **= 3:", x)  # 27
